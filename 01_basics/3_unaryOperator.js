// JavaScript Demo: Expressions - Increment operator

let x = 3
let y = x++
// console.log(`x : ${x}, y : ${y}`);
// expected output : x = 4, y = 3


let a = 3
let b = ++a
console.log(`a : ${a}, b : ${b}`)
// expected output : a = 4, b = 4
// first it will increment the value and then print the incremented value.
